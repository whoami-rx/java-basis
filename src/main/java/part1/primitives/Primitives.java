package part1.primitives;

/**
 * Primitive: is a data type
 *
 * Java have 8 primitives:
 *
 * Signed Integers: byte, short, int, long
 * Float Point: float, double
 * Character: char
 * Boolean: boolean
 */
public class Primitives {
    public static void signedIntegers() {
        byte a = 0;
        short b = 1;
        int c = 1;
        long d = 1;
    }

    public static void floatPoint() {
        float a = 1.0f;    // should have postfix 'f'
        double b = 1.0;
    }

    public static void charType() {
        char a = '\t';
        char b = '\n';
    }

    public static void boolType() {
        boolean a = true;
        boolean b = false;
    }
}
