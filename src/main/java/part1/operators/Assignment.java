package part1.operators;

public class Assignment {

    public static void main(String[] args) {
        int a = 1;
        System.out.println("a = " + a);
        a += 1;
        System.out.println("a = " + a);
        a *= 2;
        System.out.println("a = " + a);
        a -= 1;
        System.out.println("a = " + a);
        a /= 2;
        System.out.println("a = " + a);
    }
}
